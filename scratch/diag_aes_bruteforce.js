const crypto = require('crypto');
const fs = require('fs');

const rpf = fs.readFileSync('/tmp/uploaded_user.rpf');
const awcRaw = rpf.slice(2 * 512, 2 * 512 + 16); // first 16 bytes of lmg_combat.awc

const keys = [
    // PC AES Key
    Buffer.from([0x22, 0x7E, 0x14, 0x2C, 0x45, 0x5F, 0x1F, 0x18, 0x2E, 0x3E, 0x19, 0x6D, 0x32, 0x36, 0x53, 0x28, 0x2D, 0x73, 0x3A, 0x01, 0x60, 0x14, 0x6E, 0x56, 0x31, 0x72, 0x08, 0x46, 0x3E, 0x31, 0x5D, 0x41]),
    // PS3 AES Key
    Buffer.from([0x5A, 0x31, 0x24, 0x56, 0x72, 0x47, 0x4E, 0x33, 0x5B, 0x2B, 0x35, 0x58, 0x75, 0x45, 0x77, 0x43, 0x48, 0x39, 0x2F, 0x4C, 0x3B, 0x4E, 0x28, 0x4A, 0x6E, 0x61, 0x3D, 0x2D, 0x51, 0x50, 0x41, 0x37]),
    // Xbox 360 AES Key
    Buffer.from([0x52, 0x5A, 0x38, 0x49, 0x31, 0x34, 0x44, 0x51, 0x4E, 0x38, 0x35, 0x2C, 0x37, 0x2C, 0x46, 0x4E, 0x62, 0x5F, 0x3D, 0x3F, 0x2E, 0x54, 0x5E, 0x3C, 0x4D, 0x41, 0x40, 0x3A, 0x2F, 0x44, 0x45, 0x2A])
];

for (let i = 0; i < keys.length; i++) {
    try {
        const d = crypto.createDecipheriv('aes-256-ecb', keys[i], null);
        d.setAutoPadding(false);
        const dec = Buffer.concat([d.update(awcRaw), d.final()]);
        console.log(`Key ${i}: ${dec.toString('hex')} | string: ${dec.toString('ascii').replace(/[^\x20-\x7E]/g, '.')}`);
    } catch(e) {}
}

// Try AES CBC with zero IV
for (let i = 0; i < keys.length; i++) {
    try {
        const d = crypto.createDecipheriv('aes-256-cbc', keys[i], Buffer.alloc(16, 0));
        d.setAutoPadding(false);
        const dec = Buffer.concat([d.update(awcRaw), d.final()]);
        console.log(`CBC Key ${i}: ${dec.toString('hex')} | string: ${dec.toString('ascii').replace(/[^\x20-\x7E]/g, '.')}`);
    } catch(e) {}
}

